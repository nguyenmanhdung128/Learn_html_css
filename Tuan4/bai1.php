    <form method="post">
        <label for="person1_name">Họ tên người thứ nhất:</label>
        <input type="text" name="person1_name" id="person1_name" required>
        <br>
        <label for="person1_dob">Ngày sinh người thứ nhất (YYYY-MM-DD):</label>
        <input type="date" name="person1_dob" id="person1_dob" required>
        <br>
        <label for="person2_name">Họ tên người thứ hai:</label>
        <input type="text" name="person2_name" id="person2_name" required>
        <br>
        <label for="person2_dob">Ngày sinh người thứ hai (YYYY-MM-DD):</label>
        <input type="date" name="person2_dob" id="person2_dob" required>
        <br>
        <input type="submit" name="calculate" value="Tính toán">
    </form>

    <?php
    if (isset($_POST['calculate'])) {
        // Lấy thông tin người thứ nhất
        $person1_name = $_POST['person1_name'];
        $person1_dob = new DateTime($_POST['person1_dob']);

        // Lấy thông tin người thứ hai
        $person2_name = $_POST['person2_name'];
        $person2_dob = new DateTime($_POST['person2_dob']);

        // Tính tuổi của từng người
        $currentDate = new DateTime();
        $person1_age = $currentDate->diff($person1_dob)->y;
        $person2_age = $currentDate->diff($person2_dob)->y;

        // Tính số ngày chênh lệch
        $dateDiff = $person1_dob->diff($person2_dob);
        $daysDiff = $dateDiff->days;

        // Hiển thị kết quả
        echo "<h2>Kết quả:</h2>";
        echo "<p>Tuổi của $person1_name là $person1_age tuổi.</p>";
        echo "<p>Tuổi của $person2_name là $person2_age tuổi.</p>";
        echo "<p>Số ngày chênh lệch giữa họ là $daysDiff ngày.</p>";
    }
    ?>
